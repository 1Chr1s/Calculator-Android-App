package com.nerdyrocks.calculator;


public class calculations {

    /**
     * x+y
     * @param x -int
     * @param y -int
     * @return -int
     */
    public int add(int x, int y){
        return x + y;
    }

    /**
     * x-y
     * @param x -int
     * @param y -int
     * @return -int
     */
    public int subtract(int x, int y){
        return x - y;
    }

    /**
     * x*y
     * @param x -int
     * @param y -int
     * @return -int
     */
    public int multiply(int x, int y){
        return x * y;
    }

    /**
     * x/y
     * @param x -int
     * @param y -int
     * @return -int
     */
    public int divide(int x, int y){
        return x/y;
    }
}
